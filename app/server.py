def can_access_campaign(user: dict, membership: dict | None) -> bool:
    return user["global_role"] == "admin" or bool(membership)


def can_manage_campaign(user: dict, membership: dict | None) -> bool:
    return user["global_role"] == "admin" or bool(membership and membership["role"] == "dm")


def can_edit_page(user: dict, membership: dict | None, page: dict) -> bool:
    if can_manage_campaign(user, membership):
        return True
    if not membership:
        return False
    return page.get("owner_id") == user["id"]


def can_read_page(db: psycopg.Connection, user: dict, membership: dict | None, page: dict) -> bool:
    if not can_access_campaign(user, membership):
        return False
    if can_manage_campaign(user, membership):
        return True
    if page["owner_id"] == user["id"]:
        return True
    if page["category"] in DM_ONLY_CATEGORIES or page["visibility"] == "dm":
        return False
    if membership and not can_read_category(db, page["campaign_id"], page["category"], membership["role"]):
        return False
    if page["visibility"] == "private":
        return False
    if page["visibility"] == "restricted":
        acl = db.execute(
            "SELECT can_read FROM page_permissions WHERE page_id = %s AND user_id = %s",
            (page["id"], user["id"]),
        ).fetchone()
        return bool(acl and acl["can_read"])
    return True


def can_read_category(db: psycopg.Connection, campaign_id: int, category: str, role: str) -> bool:
    permission = db.execute(
        """
        SELECT can_read
        FROM category_permissions
        WHERE campaign_id = %s AND category = %s AND role = %s
        """,
        (campaign_id, category, role),
    ).fetchone()
    return True if permission is None else bool(permission["can_read"])


def visible_attachments(db: psycopg.Connection, user: dict, membership: dict | None, pages: list[dict], notes: list[dict]) -> list[dict]:
    page_ids = [page["id"] for page in pages] or [0]
    note_ids = [note["id"] for note in notes] or [0]
    rows = db.execute(
        """
        SELECT id, campaign_id, page_id, note_id, filename, mime_type, created_at
        FROM attachments
        WHERE page_id = ANY(%s) OR note_id = ANY(%s)
        ORDER BY created_at DESC
        """,
        (page_ids, note_ids),
    ).fetchall()
    return [row_to_dict(row) for row in rows]


def readers_by_page(db: psycopg.Connection, page_ids: list[int]) -> dict[str, list[int]]:
    if not page_ids:
        return {}
    rows = db.execute(
        "SELECT page_id, user_id FROM page_permissions WHERE page_id = ANY(%s) AND can_read = true",
        (page_ids,),
    ).fetchall()
    result: dict[str, list[int]] = {}
    for row in rows:
        result.setdefault(str(row["page_id"]), []).append(row["user_id"])
    return result


def save_page_readers(db: psycopg.Connection, page_id: int, reader_ids: list) -> None:
    db.execute("DELETE FROM page_permissions WHERE page_id = %s", (page_id,))
    clean_ids = sorted({int(user_id) for user_id in reader_ids if str(user_id).isdigit()})
    with db.cursor() as cursor:
        cursor.executemany(
            "INSERT INTO page_permissions (page_id, user_id, can_read) VALUES (%s, %s, true)",
            [(page_id, user_id) for user_id in clean_ids],
        )


def backfill_page_slugs(db: psycopg.Connection) -> None:
    rows = [row_to_dict(row) for row in db.execute("SELECT id, campaign_id, title, slug FROM pages ORDER BY campaign_id, id")]
    used: dict[int, set[str]] = {}
    for row in rows:
        campaign_id = row["campaign_id"]
        used.setdefault(campaign_id, set())
        base = slugify(row.get("slug") or row["title"]) or f"seite-{row['id']}"
        slug = base
        counter = 2
        while slug in used[campaign_id]:
            slug = f"{base}-{counter}"
            counter += 1
        used[campaign_id].add(slug)
        if row.get("slug") != slug:
            db.execute("UPDATE pages SET slug = %s WHERE id = %s", (slug, row["id"]))


def slugify(value: str) -> str:
    normalized = unicodedata.normalize("NFKD", clean(value)).encode("ascii", "ignore").decode("ascii")
    slug = re.sub(r"[^a-zA-Z0-9]+", "-", normalized.lower()).strip("-")
    return slug or "seite"


def unique_page_slug(db: psycopg.Connection, campaign_id: int, value: str, page_id: int | None = None) -> str:
    base = slugify(value)
    slug = base
    counter = 2
    while True:
        params: tuple = (campaign_id, slug) if page_id is None else (campaign_id, slug, page_id)
        query = "SELECT 1 FROM pages WHERE campaign_id = %s AND slug = %s"
        if page_id is not None:
            query += " AND id <> %s"
        if not db.execute(query, params).fetchone():
            return slug
        slug = f"{base}-{counter}"
        counter += 1


def normalize_tag_names(tags: object) -> list[str]:
    if isinstance(tags, str):
        candidates = tags.split(",")
    elif isinstance(tags, list):
        candidates = tags
    else:
        candidates = []
    result = []
    seen = set()
    for value in candidates:
        name = clean(value)
        key = name.lower()
        if name and key not in seen:
            seen.add(key)
            result.append(name[:60])
    return result


def save_page_tags(db: psycopg.Connection, campaign_id: int, page_id: int, tags: object) -> None:
    db.execute("DELETE FROM page_tags WHERE page_id = %s", (page_id,))
    for name in normalize_tag_names(tags):
        slug = slugify(name)
        tag = db.execute(
            """
            INSERT INTO tags (campaign_id, name, slug, created_at)
            VALUES (%s, %s, %s, %s)
            ON CONFLICT (campaign_id, slug) DO UPDATE SET name = excluded.name
            RETURNING id
            """,
            (campaign_id, name, slug, now()),
        ).fetchone()
        db.execute(
            "INSERT INTO page_tags (page_id, tag_id) VALUES (%s, %s) ON CONFLICT DO NOTHING",
            (page_id, tag["id"]),
        )


def save_page_relations(db: psycopg.Connection, campaign_id: int, page_id: int, relations: object, can_store_private: bool) -> None:
    db.execute("DELETE FROM page_relations WHERE source_page_id = %s", (page_id,))
    if not isinstance(relations, list):
        return
    for relation in relations:
        if not isinstance(relation, dict):
            continue
        target_title = clean(relation.get("target_title"))
        target_id = relation.get("target_page_id")
        target = None
        if str(target_id or "").isdigit():
            target = db.execute(
                "SELECT id, title FROM pages WHERE campaign_id = %s AND id = %s",
                (campaign_id, int(target_id)),
            ).fetchone()
        if not target and target_title:
            target = find_page_by_title_or_slug(db, campaign_id, target_title)
        if target:
            target_id = target["id"]
            target_title = target["title"]
        if not target_title:
            continue
        visibility = validated(relation.get("visibility"), PAGE_VISIBILITIES, "players")
        if visibility in ("dm", "private") and not can_store_private:
            visibility = "players"
        db.execute(
            """
            INSERT INTO page_relations (
                campaign_id, source_page_id, target_page_id, target_title,
                relation_type, description, visibility, created_at
            )
            VALUES (%s, %s, %s, %s, %s, %s, %s, %s)
            """,
            (
                campaign_id,
                page_id,
                int(target_id) if str(target_id or "").isdigit() else None,
                target_title[:160],
                clean(relation.get("relation_type")) or "related",
                clean(relation.get("description")),
                visibility,
                now(),
            ),
        )


def find_page_by_title_or_slug(db: psycopg.Connection, campaign_id: int, value: str) -> dict | None:
    lookup = clean(value)
    return db.execute(
        """
        SELECT id, title, slug
        FROM pages
        WHERE campaign_id = %s AND (lower(title) = lower(%s) OR slug = %s)
        ORDER BY id
        LIMIT 1
        """,
        (campaign_id, lookup, slugify(lookup)),
    ).fetchone()


def sync_page_mentions(db: psycopg.Connection, campaign_id: int, page_id: int, public_body: str, private_body: str, page_visibility: str) -> None:
    db.execute("DELETE FROM page_mentions WHERE source_page_id = %s", (page_id,))
    mention_sources = [
        ("body", public_body or "", page_visibility),
        ("body_private", private_body or "", "dm"),
    ]
    seen = set()
    for source_field, markdown, visibility in mention_sources:
        for raw in MENTION_RE.findall(markdown):
            target_title = clean(raw.split("|", 1)[0])
            if not target_title:
                continue
            key = (source_field, target_title.lower())
            if key in seen:
                continue
            seen.add(key)
            target = find_page_by_title_or_slug(db, campaign_id, target_title)
            db.execute(
                """
                INSERT INTO page_mentions (
                    campaign_id, source_page_id, target_page_id, target_title,
                    source_field, visibility, created_at
                )
                VALUES (%s, %s, %s, %s, %s, %s, %s)
                """,
                (
                    campaign_id,
                    page_id,
                    target["id"] if target else None,
                    target["title"] if target else target_title[:160],
                    source_field,
                    visibility,
                    now(),
                ),
            )



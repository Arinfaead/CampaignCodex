def enrich_pages(db: psycopg.Connection, pages: list[dict], user: dict, membership: dict | None) -> list[dict]:
    if not pages:
        return []
    page_ids = [page["id"] for page in pages]
    visible_ids = set(page_ids)
    manageable = can_manage_campaign(user, membership)
    tags_by_page = page_tags_map(db, page_ids)
    relations_by_source = page_relations_map(db, page_ids, visible_ids, manageable)
    backlinks_by_target = page_backlinks_map(db, page_ids, visible_ids, manageable)
    mentions_by_source = page_mentions_map(db, page_ids, manageable)
    result = []
    for page in pages:
        page = dict(page)
        if not manageable:
            page["body_private"] = ""
        page["tags"] = tags_by_page.get(page["id"], [])
        page["relations"] = relations_by_source.get(page["id"], [])
        page["backlinks"] = backlinks_by_target.get(page["id"], [])
        page["mentions"] = mentions_by_source.get(page["id"], [])
        result.append(page)
    return result


def page_tags_map(db: psycopg.Connection, page_ids: list[int]) -> dict[int, list[str]]:
    rows = db.execute(
        """
        SELECT page_tags.page_id, tags.name
        FROM page_tags
        JOIN tags ON tags.id = page_tags.tag_id
        WHERE page_tags.page_id = ANY(%s)
        ORDER BY tags.name
        """,
        (page_ids,),
    ).fetchall()
    result: dict[int, list[str]] = {}
    for row in rows:
        result.setdefault(row["page_id"], []).append(row["name"])
    return result


def page_relations_map(db: psycopg.Connection, page_ids: list[int], visible_ids: set[int], manageable: bool) -> dict[int, list[dict]]:
    rows = db.execute(
        """
        SELECT page_relations.*, pages.title AS resolved_target_title, pages.slug AS target_slug
        FROM page_relations
        LEFT JOIN pages ON pages.id = page_relations.target_page_id
        WHERE source_page_id = ANY(%s)
        ORDER BY relation_type, target_title
        """,
        (page_ids,),
    ).fetchall()
    result: dict[int, list[dict]] = {}
    for row in rows:
        relation = row_to_dict(row)
        if not manageable and relation["visibility"] in ("dm", "private"):
            continue
        if relation["target_page_id"] and relation["target_page_id"] not in visible_ids:
            continue
        relation["target_title"] = relation["resolved_target_title"] or relation["target_title"]
        result.setdefault(relation["source_page_id"], []).append(relation)
    return result


def page_backlinks_map(db: psycopg.Connection, page_ids: list[int], visible_ids: set[int], manageable: bool) -> dict[int, list[dict]]:
    rows = db.execute(
        """
        SELECT page_mentions.*, pages.title AS source_title, pages.slug AS source_slug
        FROM page_mentions
        JOIN pages ON pages.id = page_mentions.source_page_id
        WHERE page_mentions.target_page_id = ANY(%s)
        ORDER BY pages.title
        """,
        (page_ids,),
    ).fetchall()
    result: dict[int, list[dict]] = {}
    for row in rows:
        backlink = row_to_dict(row)
        if backlink["source_page_id"] not in visible_ids:
            continue
        if not manageable and backlink["visibility"] in ("dm", "private"):
            continue
        result.setdefault(backlink["target_page_id"], []).append(backlink)
    return result


def page_mentions_map(db: psycopg.Connection, page_ids: list[int], manageable: bool) -> dict[int, list[dict]]:
    rows = db.execute(
        """
        SELECT page_mentions.*, pages.slug AS target_slug
        FROM page_mentions
        LEFT JOIN pages ON pages.id = page_mentions.target_page_id
        WHERE source_page_id = ANY(%s)
        ORDER BY target_title
        """,
        (page_ids,),
    ).fetchall()
    result: dict[int, list[dict]] = {}
    for row in rows:
        mention = row_to_dict(row)
        if not manageable and mention["visibility"] in ("dm", "private"):
            continue
        result.setdefault(mention["source_page_id"], []).append(mention)
    return result



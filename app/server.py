def hash_password(password: str) -> str:
    salt = secrets.token_hex(16)
    iterations = 260000
    digest = hashlib.pbkdf2_hmac("sha256", password.encode("utf-8"), salt.encode("utf-8"), iterations)
    return f"pbkdf2_sha256${iterations}${salt}${digest.hex()}"


def verify_password(password: str, stored: str | None) -> bool:
    if not stored:
        return False
    algorithm, iterations, salt, expected = stored.split("$", 3)
    if algorithm != "pbkdf2_sha256":
        return False
    digest = hashlib.pbkdf2_hmac("sha256", password.encode("utf-8"), salt.encode("utf-8"), int(iterations))
    return hmac.compare_digest(digest.hex(), expected)


def hash_token(token: str) -> str:
    return hashlib.sha256(token.encode("utf-8")).hexdigest()


def make_excerpt(body: str, term: str) -> str:
    index = body.lower().find(term.lower())
    if index < 0:
        return body[:180]
    start = max(0, index - 70)
    end = min(len(body), index + 140)
    return body[start:end]


def make_page_excerpt(item: dict, term: str, include_private: bool) -> str:
    body = item.get("body", "") or ""
    if term.lower() in body.lower():
        return make_excerpt(body, term)
    private_body = item.get("body_private", "") or ""
    if include_private and term.lower() in private_body.lower():
        return make_excerpt(private_body, term)
    return make_excerpt(body or item.get("summary", ""), term)


def clean(value: object) -> str:
    return str(value or "").strip()


def validated(value: object, allowed: tuple[str, ...], fallback: str) -> str:
    candidate = clean(value)
    return candidate if candidate in allowed else fallback


def sql_tuple(values: tuple[str, ...]) -> str:
    return "(" + ", ".join(f"'{value}'" for value in values) + ")"



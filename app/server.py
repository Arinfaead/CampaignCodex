def export_campaign(db: psycopg.Connection, campaign_id: int) -> dict:
    campaign = row_to_dict(db.execute("SELECT * FROM campaigns WHERE id = %s", (campaign_id,)).fetchone())
    pages = [row_to_dict(row) for row in db.execute("SELECT * FROM pages WHERE campaign_id = %s ORDER BY id", (campaign_id,))]
    notes = [row_to_dict(row) for row in db.execute("SELECT * FROM session_notes WHERE campaign_id = %s ORDER BY id", (campaign_id,))]
    memberships = [row_to_dict(row) for row in db.execute("SELECT * FROM memberships WHERE campaign_id = %s", (campaign_id,))]
    tags = [row_to_dict(row) for row in db.execute("SELECT * FROM tags WHERE campaign_id = %s ORDER BY id", (campaign_id,))]
    page_tags = [
        row_to_dict(row)
        for row in db.execute(
            """
            SELECT page_tags.*
            FROM page_tags
            JOIN pages ON pages.id = page_tags.page_id
            WHERE pages.campaign_id = %s
            ORDER BY page_tags.page_id, page_tags.tag_id
            """,
            (campaign_id,),
        )
    ]
    page_relations = [row_to_dict(row) for row in db.execute("SELECT * FROM page_relations WHERE campaign_id = %s ORDER BY id", (campaign_id,))]
    attachments = []
    for row in db.execute("SELECT * FROM attachments WHERE campaign_id = %s ORDER BY id", (campaign_id,)):
        item = row_to_dict(row)
        item["content_base64"] = base64.b64encode(bytes(item.pop("content"))).decode("ascii")
        attachments.append(item)
    return {
        "format": "campaigncodex.v2",
        "exported_at": now(),
        "campaign": campaign,
        "pages": pages,
        "tags": tags,
        "page_tags": page_tags,
        "page_relations": page_relations,
        "notes": notes,
        "memberships": memberships,
        "attachments": attachments,
    }


def import_campaign(db: psycopg.Connection, user: dict, payload: dict) -> int:
    data = payload.get("data", payload)
    campaign = data["campaign"]
    timestamp = now()
    new_campaign_id = db.execute(
        "INSERT INTO campaigns (name, system, description, created_at) VALUES (%s, %s, %s, %s) RETURNING id",
        (
            f"{campaign['name']} Import",
            campaign.get("system", "D&D 5e"),
            campaign.get("description", ""),
            timestamp,
        ),
    ).fetchone()["id"]
    db.execute(
        "INSERT INTO memberships (user_id, campaign_id, role) VALUES (%s, %s, 'dm')",
        (user["id"], new_campaign_id),
    )
    page_id_map = {}
    note_id_map = {}
    tag_id_map = {}
    for page in data.get("pages", []):
        title = page.get("title", "Importierte Seite")
        new_page = db.execute(
            """
            INSERT INTO pages (
                campaign_id, owner_id, title, slug, category, template_key, summary, visibility,
                body, body_private, status, metadata, created_at, updated_at
            )
            VALUES (%s, %s, %s, %s, %s, %s, %s, %s, %s, %s, %s, %s::jsonb, %s, %s)
            RETURNING id
            """,
            (
                new_campaign_id,
                user["id"],
                title,
                unique_page_slug(db, new_campaign_id, page.get("slug") or title),
                page["category"],
                page.get("template_key", "generic"),
                page.get("summary", ""),
                page.get("visibility", "players"),
                page.get("body", ""),
                page.get("body_private", ""),
                page.get("status", ""),
                json.dumps(page.get("metadata") or {}),
                timestamp,
                timestamp,
            ),
        ).fetchone()
        page_id_map[page["id"]] = new_page["id"]
    for page in data.get("pages", []):
        if page.get("parent_id") in page_id_map:
            db.execute("UPDATE pages SET parent_id = %s WHERE id = %s", (page_id_map[page["parent_id"]], page_id_map[page["id"]]))
    for tag in data.get("tags", []):
        new_tag = db.execute(
            """
            INSERT INTO tags (campaign_id, name, slug, color, created_at)
            VALUES (%s, %s, %s, %s, %s)
            ON CONFLICT (campaign_id, slug) DO UPDATE SET name = excluded.name, color = excluded.color
            RETURNING id
            """,
            (
                new_campaign_id,
                tag.get("name", "Tag"),
                tag.get("slug") or slugify(tag.get("name", "Tag")),
                tag.get("color", ""),
                timestamp,
            ),
        ).fetchone()
        tag_id_map[tag["id"]] = new_tag["id"]
    for page_tag in data.get("page_tags", []):
        if page_tag.get("page_id") in page_id_map and page_tag.get("tag_id") in tag_id_map:
            db.execute(
                "INSERT INTO page_tags (page_id, tag_id) VALUES (%s, %s) ON CONFLICT DO NOTHING",
                (page_id_map[page_tag["page_id"]], tag_id_map[page_tag["tag_id"]]),
            )
    for relation in data.get("page_relations", []):
        if relation.get("source_page_id") not in page_id_map:
            continue
        target_page_id = page_id_map.get(relation.get("target_page_id"))
        db.execute(
            """
            INSERT INTO page_relations (
                campaign_id, source_page_id, target_page_id, target_title,
                relation_type, description, visibility, created_at
            )
            VALUES (%s, %s, %s, %s, %s, %s, %s, %s)
            """,
            (
                new_campaign_id,
                page_id_map[relation["source_page_id"]],
                target_page_id,
                relation.get("target_title", ""),
                relation.get("relation_type", "related"),
                relation.get("description", ""),
                validated(relation.get("visibility"), PAGE_VISIBILITIES, "players"),
                timestamp,
            ),
        )
    for page in data.get("pages", []):
        if page.get("id") in page_id_map:
            sync_page_mentions(
                db,
                new_campaign_id,
                page_id_map[page["id"]],
                page.get("body", ""),
                page.get("body_private", ""),
                page.get("visibility", "players"),
            )
    for note in data.get("notes", []):
        new_note = db.execute(
            """
            INSERT INTO session_notes (campaign_id, user_id, title, body, updated_at)
            VALUES (%s, %s, %s, %s, %s)
            RETURNING id
            """,
            (
                new_campaign_id,
                user["id"],
                note.get("title", "Importierte Notiz"),
                note.get("body", ""),
                timestamp,
            ),
        ).fetchone()
        note_id_map[note["id"]] = new_note["id"]
    for attachment in data.get("attachments", []):
        old_page_id = attachment.get("page_id")
        old_note_id = attachment.get("note_id")
        if old_page_id and old_page_id not in page_id_map:
            continue
        if old_note_id and old_note_id not in note_id_map:
            continue
        db.execute(
            """
            INSERT INTO attachments (campaign_id, page_id, note_id, uploaded_by, filename, mime_type, content, created_at)
            VALUES (%s, %s, %s, %s, %s, %s, %s, %s)
            """,
            (
                new_campaign_id,
                page_id_map.get(old_page_id),
                note_id_map.get(old_note_id),
                user["id"],
                attachment["filename"],
                attachment.get("mime_type", "application/octet-stream"),
                base64.b64decode(attachment.get("content_base64", "")),
                timestamp,
            ),
        )
    return new_campaign_id


